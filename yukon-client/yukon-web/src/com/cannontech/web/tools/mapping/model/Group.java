package com.cannontech.web.tools.mapping.model;

public class Group {

    private int id; // state group id
    private int state; // raw state
    
    public int getId() {
        return id;
    }
    
    public void setId(int id) {
        this.id = id;
    }
    
    public int getState() {
        return state;
    }
    
    public void setState(int state) {
        this.state = state;
    }
    
}