package com.cannontech.cbc.cyme;

import java.util.List;

import com.cannontech.cbc.cyme.model.CymeSimulationStatus;
import com.cannontech.cbc.cyme.model.SimulationResultSummaryData;


public interface CymeWebService {

    /**
     * Takes an XML formatted string which represents changes to make to a CYME model 
     * and will run a load simulation on the model with the changes.
     * 
     * @param xmlData
     * @return simulationId for subsequent calls to get the reports.
     */
    public String runSimulation(String xmlData);

    /**
     * Returns simulation status from CYME for the simulation with simulationId.
     * 
     * If the simulation status does not exist, it will throw a NotFoundException.
     * @param simulationId
     */
    public CymeSimulationStatus getSimulationReportStatus(String simulationId);

    /**
     * Returns list of reports generated by the simluation.
     * 
     * @param simulationId
     * @return
     */
    public List<SimulationResultSummaryData> generateResultSummary(String simulationId);
    
    /**
     * Returns the XML formatted report for the simulation that matches the SimulationResultSummaryData.
     * 
     * @param simulationSummary
     * @return
     */
    public String getSimulationReport(SimulationResultSummaryData simulationSummary);
}
