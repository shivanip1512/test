{
   if(this != &aRef)
   {
   }
   return *this;
}

//=========================================================================================================================================
//=========================================================================================================================================

int CtiAnsiTableOneTwo::getIdCode( void )
{
   return uom_entries->id_code;
}

//=========================================================================================================================================
//=========================================================================================================================================

int CtiAnsiTableOneTwo::getTimeBase( void )
{
   return uom_entries->time_base;
}

//=========================================================================================================================================
//=========================================================================================================================================

int CtiAnsiTableOneTwo::getMultiplier( void )
{
   return uom_entries->multiplier;
}

