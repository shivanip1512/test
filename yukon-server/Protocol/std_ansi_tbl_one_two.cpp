CtiAnsiTableOneTwo::CtiAnsiTableOneTwo( BYTE *dataBlob, int num_uom_entries )
{
   uom_entries = new UOM_ENTRY_BFLD[num_uom_entries];

   memcpy( uom_entries, dataBlob, sizeof( uom_entries ));
}

//=========================================================================================================================================
//=========================================================================================================================================

CtiAnsiTableOneTwo::~CtiAnsiTableOneTwo()
{
   delete []uom_entries;
}

